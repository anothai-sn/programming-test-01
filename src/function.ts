// Create funciton input A, B and return A+B 
function Add(A:number, B:number) {
    return A+B
} 
console.log(`A + B = ${Add(2, 3)}`) // Output 2 + 3 

// Create function input A, B, C and return A + B + C is a sum of parameters and /3 for find average
function AVG(A:number, B:number, C:number) {
    return (A+B+C)/3
}
console.log(`Average of A, B, C : ${AVG(2, 3, 4)}`) // Output average 2, 3, 4